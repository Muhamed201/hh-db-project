-- Индексы для группировки по регионам (Задание 3)
-- В таблице 10к вакансий и 100к резюме. Без этих индексов базе пришлось бы 
-- перебирать каждую строку, чтобы сосчитать среднюю зарплату для каждого региона.
-- Индекс работает как алфавитный указатель: база сразу видит все записи нужного региона в одной "пачке".
CREATE INDEX idx_vacancies_area_id ON vacancies(area_id);
CREATE INDEX idx_resumes_area_id ON resumes(area_id);

-- Индексы для анализа по датам (Задание 4)
-- Запрос ищет месяц с пиком активности. Индекс хранит даты в отсортированном виде (B-Tree).
-- Это позволяет базе мгновенно переходить к нужному диапазону дат, не просматривая данные за весь год.
CREATE INDEX idx_vacancies_published_at ON vacancies(published_at);
CREATE INDEX idx_resumes_published_at ON resumes(published_at);

-- Индекс на внешний ключ для связи вакансий и откликов (Задание 5)
-- В таблице responses 150к+ записей. При JOIN базе нужно 
-- для каждой вакансии найти её отклики. Индекс превращает долгий перебор всех 150к строк 
-- в быстрый точечный поиск нужных ID.
CREATE INDEX idx_responses_vacancy_id ON responses(vacancy_id);

-- Индекс для фильтрации откликов (Задание 5)
-- Мы ищем отклики за первую неделю после публикации. Индекс на дату отклика 
-- помогает базе быстро отсечь все старые записи и работать только с актуальным временным окном.
CREATE INDEX idx_responses_applied_at ON responses(applied_at);